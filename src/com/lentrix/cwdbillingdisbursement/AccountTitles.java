    private void addAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAccountButtonActionPerformed
        selected = new AccountTitle();
        updateView();
        accountNumberField.grabFocus();
    }//GEN-LAST:event_addAccountButtonActionPerformed

    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        selected.setAccountNumber(accountNumberField.getText());
        selected.setAccountTitle(accountTitleField.getText());
        if(selected.isValid()) {
            try {
                selected.save(user);
                refreshTable();
            } catch (SQLException ex) {
                Base.error(ex.getMessage(), this);
                ex.printStackTrace();
            }
        }else {
            Base.error("You have an invalid entry.\nPlease check and try again.", this);
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Object selectedRow = jTable1.getValueAt(jTable1.getSelectedRow(), 1);
        if(selectedRow!=null) {
            selected = (AccountTitle)selectedRow;
            updateView();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void removeAccountTitleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeAccountTitleActionPerformed
        int response = JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to delete the " 
                        + selected.getAccountTitle() 
                        + " account?", "Delete?", JOptionPane.YES_NO_OPTION);
        if(response==JOptionPane.YES_OPTION) {
            try {
                selected.delete(user);
                refreshTable();
                selected = new AccountTitle();
                updateView();
            } catch (SQLException ex) {
                Base.error(ex.getMessage(), this);
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_removeAccountTitleActionPerformed

    private void updateView() {
        accountNumberField.setText(selected.getAccountNumber());
        accountTitleField.setText(selected.getAccountTitle());
    }
    
