    private void refreshTable() {
        try{
            AccountTitle[] accTitles = AccountTitle.loadAll("");
            
            Object content[][] = new Object[accTitles.length][2];
            String cols[] = {"Account Number", "Account Title"};
            
            for(int i=0; i<accTitles.length; i++) {
                content[i][0] = accTitles[i].getAccountNumber();
                content[i][1] = accTitles[i];
            }
            
            DefaultTableModel model = new DefaultTableModel(content, cols){

                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
                
            };
            
            jTable1.setModel(model);
        }catch(Exception ex) {
            Base.error(ex.getMessage(), this);
            ex.printStackTrace();
        }
    }

